const month = "marchhh"

switch (month) {
    case "jan":
        console.log("january");
        break;
    case "Feb":
        console.log("february");
        break;
    case "marchhh":
        console.log("march");
        break;
    case "April":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}